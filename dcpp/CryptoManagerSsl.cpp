// TLS ≤1.2 cipher string for SSL_CTX_set_cipher_list.
static const char ciphersuites[] =
        "ECDHE-ECDSA-AES256-GCM-SHA384:ECDHE-ECDSA-CHACHA20-POLY1305:"
        "ECDHE-ECDSA-AES128-GCM-SHA256:"
        "ECDHE-ECDSA-AES256-SHA384:ECDHE-ECDSA-AES128-SHA256:"
        "ECDHE-RSA-AES256-GCM-SHA384:ECDHE-RSA-CHACHA20-POLY1305:"
        "ECDHE-RSA-AES128-GCM-SHA256:"
        "ECDHE-RSA-AES256-SHA384:ECDHE-RSA-AES128-SHA256:"
        "ECDHE-ECDSA-AES256-SHA:ECDHE-ECDSA-AES128-SHA:"
        "ECDHE-RSA-AES256-SHA:ECDHE-RSA-AES128-SHA:"
        "DHE-RSA-AES256-SHA:DHE-RSA-AES128-SHA:"
        "AES256-GCM-SHA384:AES256-SHA256:AES256-SHA:AES128-SHA"
        "!aNULL:!eNULL:!EXPORT:!DES:!RC4:!3DES:!MD5:!PSK";

static void setContextCiphers(SSL_CTX* ctx) {
    SSL_CTX_set_cipher_list(ctx, ciphersuites);
    // TLS 1.3 API: OpenSSL 1.1.1+; LibreSSL 3.4.1+ (LibreSSL's OPENSSL_VERSION_NUMBER is 0x20000000).
#if (!defined(LIBRESSL_VERSION_NUMBER) && OPENSSL_VERSION_NUMBER >= 0x10101000L) || \
    (defined(LIBRESSL_VERSION_NUMBER) && LIBRESSL_VERSION_NUMBER >= 0x3040100fL)
    SSL_CTX_set_ciphersuites(ctx,
            "TLS_AES_256_GCM_SHA384:TLS_CHACHA20_POLY1305_SHA256:TLS_AES_128_GCM_SHA256");
#endif
}

